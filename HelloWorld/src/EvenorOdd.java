
public class EvenorOdd {

	public static void main(String[] args) {
		int n=8;
		if(n%2==0)
		{
			System.out.println("Even Number");
		}
		else if(n%2==1)
		{
			System.out.println("Odd number");
		}
		else
		{
			System.out.println("nothing");
			System.out.println("BYe");
		}

	}

}
