
public class OperatorDemo  {

	public static void main(String[] args) {
		int m=6;
		int n=8;
		int r1 = m+n;
		int r2 = n-m;
		int r3 = m*n;
		int r4 = n/m;
		int r5 = n%m;
		n++;
		System.out.println(n);
		
		System.out.println(r1 +" \n"+r2+" \n"+r3+" \n"+r4+" \n"+r5);
		
	}

}
