
public class IterationDemo
{

	public static void main(String[] args) 
	{
		for(int i=1;i<=5;i++)
		{
			System.out.println(i+" .Hello");
		}
		int j = 1;
		while(j<=5)
		{
			System.out.println(j+ " .Sad");
			j++;
		}
		int k =1;
		do
		{
			System.out.println(k+ " .Very Hungery");
			k++;
		}while(k<=5);
	}

}
